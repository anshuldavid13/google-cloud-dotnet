using System;
using Xunit;
using static Google.Cloud.Firestore.AggregateField;
using static Google.Cloud.Firestore.V1.StructuredAggregationQuery.Types;
using static Google.Cloud.Firestore.V1.StructuredAggregationQuery.Types.Aggregation.Types;

namespace Google.Cloud.Firestore.Tests;
public class AggregateFieldTest
{
    [Fact]
    public void NullFieldCheck()
    {        
        Assert.Throws<ArgumentNullException>(() => Sum(null));
        Assert.Throws<ArgumentNullException>(() => Average(null));
    }

    [Fact]
    public void AggregateFieldTests()
    {
        // Alias generated by client library
        Assert.Equal(new Aggregation { Count = new Count(), Alias = CountAlias }, Count().Aggregation);
        Assert.Equal(new Aggregation { Sum = new Sum { Field = FieldPath.FromDotSeparatedString("age").ToFieldReference() }, Alias = "Sum_age" }, Sum("age").Aggregation);
        Assert.Equal(new Aggregation { Avg = new Avg { Field = FieldPath.FromDotSeparatedString("age").ToFieldReference() }, Alias = "Avg_age" }, Average("age").Aggregation);

        // Alias specified explicitly by the user
        Assert.Equal(new Aggregation { Sum = new Sum { Field = FieldPath.FromDotSeparatedString("age").ToFieldReference() }, Alias = "sum_alias" }, Sum("age", "sum_alias").Aggregation);
        Assert.Equal(new Aggregation { Avg = new Avg { Field = FieldPath.FromDotSeparatedString("age").ToFieldReference() }, Alias = "age_alias" }, Average("age", "age_alias").Aggregation);
    }
}
